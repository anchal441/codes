#include <iostream>
using namespace std;
int main(){
	int arr[50];
	int largest=-10000;
	for(int i=0;i<10;i++){
		cin>>arr[i];
		largest==arr[i];
		if(arr[i]>largest){
			largest=arr[i];
			cout<<"largest now is"<<largest<<endl;
		}
		else{
			
			cout<<"largest now is"<<largest<<endl;
		}
	}
}